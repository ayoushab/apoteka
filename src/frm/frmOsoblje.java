    private void btnIzbrisiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIzbrisiMouseClicked
        // TODO add your handling code here:
        if(txtSifra1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "Izaberite osoblje koji zelite da izbrisete!");
        }
        else{
            try{
               con = _db.KonBaza();
               String sql="DELETE FROM apoteka.user WHERE user_id ='"+txtSifra1.getText()+"'";
               
               pst =  con.prepareStatement(sql);
               pst.executeUpdate();
               JOptionPane.showMessageDialog(rootPane, "Uspesno ste obrisali!");
               tabelaOsoblje();
            }catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        }
     
    }//GEN-LAST:event_btnIzbrisiMouseClicked


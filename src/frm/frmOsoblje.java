    private void btnDodajMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDodajMouseClicked
        // TODO add your handling code here:
        String ime = txtIme.getText();
        String prezime = txtPrezime.getText();
        String brtel = txtBrTel.getText();
        String korisnickoIme = txtKorIme.getText();
        String password = String.valueOf(pswSifra.getPassword());
        String pol = cmbPol.getSelectedItem().toString();
        String uloga = cmbUloga.getSelectedItem().toString();
        
        
        if(ime.equals("") || prezime.equals("") || brtel.equals("") || korisnickoIme.equals("") || password.equals("") || pol.equals("") || uloga.equals(""))
            {
            JOptionPane.showMessageDialog(rootPane, "Popunite sva polja!","Prazna polja",JOptionPane.WARNING_MESSAGE);   
            
           }
           
        else{
           
         try {
           String query = "INSERT INTO `user`(`ime`, `prezime`, `br_tel`, `korisnicko_ime`, `password`, `pol`, `uloga` ) VALUES (?, ?, ?, ?, ?, ?,?)";
            con = _db.KonBaza();
            pst = con.prepareStatement(query);
            pst.setString(1, txtIme.getText());
            pst.setString(2, txtPrezime.getText());
            pst.setInt(3, Integer.valueOf(txtBrTel.getText()));
            pst.setString(4, txtKorIme.getText());
            pst.setString(5, String.valueOf(pswSifra.getPassword()));
            pst.setString(6, cmbPol.getSelectedItem().toString());
            pst.setString(7,cmbUloga.getSelectedItem().toString());
            
            pst.execute();
               JOptionPane.showMessageDialog(null, "Uspesno ste dodali!");
            tabelaOsoblje();
         
                }
         catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Neuspesno!" + e);
        }
        
      
        }
        
    }//GEN-LAST:event_btnDodajMouseClicked


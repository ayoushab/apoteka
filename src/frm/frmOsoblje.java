    private void btnIzmeniMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIzmeniMouseClicked
        // TODO add your handling code here:
         if(txtSifra1.getText().isEmpty() ||txtIme.getText().isEmpty() ||txtPrezime.getText().isEmpty() ||txtBrTel.getText().isEmpty()||txtKorIme.getText().isEmpty()
                 ||pswSifra.getPassword().equals("") ||cmbPol.getSelectedItem().equals("") ||cmbUloga.getSelectedItem().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Bezuspesno! Odaberite da biste izmenili podatke.");
        }
        else 
        {
            try 
            {
                
               con = _db.KonBaza();
               String sql="UPDATE apoteka.user SET user_id = '"+txtSifra1.getText()+"'"+",ime = '"+txtIme.getText()+"'"
                       + ""+",prezime = '"+txtPrezime.getText()+"'"+",br_tel = '"+txtBrTel.getText()
                       +"'"+",korisnicko_ime = '"+txtKorIme.getText()+"'"+",password = '"+pswSifra.getPassword()
                       +"'"+",pol = '"+cmbPol.getSelectedItem().toString()+"'"+",uloga = '"+cmbUloga.getSelectedItem().toString()
                       +"'"+"where user_id = "+txtSifra1.getText();    
               
               pst =  con.prepareStatement(sql);
               pst.executeUpdate();
               JOptionPane.showMessageDialog(rootPane, "Uspesno ste izmenili!");
               tabelaOsoblje();
            }
            catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
            }
            tabelaOsoblje();
        }
    }//GEN-LAST:event_btnIzmeniMouseClicked


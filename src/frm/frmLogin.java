    private void btnUlogujActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUlogujActionPerformed
        // TODO add your handling code here:
       
        String ime = txtKorisnik.getText().trim();
        String sifra = String.valueOf(pswSifra.getPassword());
        String uloga = cmbUloga.getSelectedItem().toString();
        
        
      try {
       con = _db.KonBaza();
       String sql = "SELECT * FROM `user` WHERE `korisnicko_ime`='"+ime+"' and `password`='"+sifra+"' and uloga='"+uloga+"'";
       PreparedStatement pst = con.prepareStatement(sql);
       ResultSet rs = pst.executeQuery();
       if(rs.next()) {
           //String u = rs.getString("naziv");
           if(uloga.equals("Admin")){
                JOptionPane.showMessageDialog(null, "Ulogovani kao Administrator!");
                frmOsoblje osoblje = new frmOsoblje();
                osoblje.setVisible(true);
                setVisible(false);
           }
           else if (uloga.equals("Apotekar")) {
            JOptionPane.showMessageDialog(null, "Ulogovani kao Apotekar!");
            frmProdaja prodaja = new frmProdaja();
            prodaja.lblUloga.setText("Apotekar");
            prodaja.setVisible(true);
            setVisible(false);
           }
           
        }
       else if(ime.equals("") || sifra.trim().equals("") || uloga.equals("Odaberi"))
            {
            JOptionPane.showMessageDialog(rootPane, "Popunite sva polja!","Prazna polja",JOptionPane.WARNING_MESSAGE);   
        }
       else {
           JOptionPane.showMessageDialog(null, "Korisnicko ime ili Password nisu ispravni!");
           txtKorisnik.setText("");
           pswSifra.setText("");
        
           
            }
       }
       catch(Exception e)
       {
                  JOptionPane.showMessageDialog(null, "Gre≈°ka u komunikaciji sa bazom"+e);
       }
        clearFields(e);
    }//GEN-LAST:event_btnUlogujActionPerformed


    private void txtKorisnikKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKorisnikKeyPressed
        // TODO add your handling code here:
          if(txtKorisnik.getText().isEmpty())
        {
            txtKorisnik.requestFocus();
        }
        else{
            int key = evt.getKeyCode();
            if(key == 10){
                pswSifra.requestFocus();
            }
          }
    }//GEN-LAST:event_txtKorisnikKeyPressed

    private void pswSifraKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pswSifraKeyPressed
        // TODO add your handling code here:
        if(String.valueOf(pswSifra.getPassword()).equals(""))
        {
            pswSifra.requestFocus();
        }
        else{
            int key = evt.getKeyCode();
            if(key==10){
                btnUloguj.doClick();
            }
        }
    }//GEN-LAST:event_pswSifraKeyPressed

